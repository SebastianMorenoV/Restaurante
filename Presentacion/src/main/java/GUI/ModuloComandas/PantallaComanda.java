    public void calcularTotal() {
        double total = 0.0;

        // Recorre todas las filas de la tabla
        for (int i = 0; i < tablaProductosComanda.getRowCount(); i++) {
            String precioStr = tablaProductosComanda.getValueAt(i, 2).toString();

            // Convierte el string a double (con manejo de errores)
            try {
                double precio = Double.parseDouble(precioStr);
                total += precio;
            } catch (NumberFormatException e) {
                System.err.println("Error al convertir el precio en la fila " + i + ": " + precioStr);
            }
        }

        // Calcula subtotal, IVA y propina sobre el total acumulado
        double subtotal = total / 1.16;
        double iva = total - subtotal;
        double propina = total * 0.15;

        // Muestra los resultados
        lblSubtotal.setText("Subtotal: $" + String.format("%.2f", subtotal));
        lblIVA.setText("IVA: $" + String.format("%.2f", iva));
        lblTotal.setText("Total: $" + String.format("%.2f", total));
        lblPropina.setText("Propina Sugerida (15%): $" + String.format("%.2f", propina));
    }

    public void guardarComanda() {
        CrearComandaDTO comandaDTO = new CrearComandaDTO();
        DefaultTableModel modeloTabla = (DefaultTableModel) tablaProductosComanda.getModel();
        if (modeloTabla.getRowCount() == 0) {
            JOptionPane.showMessageDialog(this, "Necesitas al menos un producto para realizar una comanda.", "Error", JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        for (int i = 0; i < modeloTabla.getRowCount(); i++) {
            ProductoDTO producto = new ProductoDTO();

            producto.setNombre((String) modeloTabla.getValueAt(i, 0));
            String tipoString = (String) modeloTabla.getValueAt(i, 1);
            Tipo tipo = Tipo.valueOf(tipoString);  // Convertir de String a enum
            producto.setTipo(tipo);
            producto.setPrecio((Double) modeloTabla.getValueAt(i, 2));

            comandaDTO.addProductoComanda(producto);

            // para cada producto de la comanda añadire su detalle obtenido desde el modulo de productos
            DetallesComandaDTO detalleComandaDTO = new DetallesComandaDTO();
            // ejemplos mockeados aqui se sustituira por los gets y sets de modulo productos en aplicacion.
            detalleComandaDTO.setComentarios("Sin jalapeño"); // app.getComentario para el producto
            detalleComandaDTO.setCantidad(1);
            detalleComandaDTO.setPrecioUnitario((Double) modeloTabla.getValueAt(i, 2));
            detalleComandaDTO.setImporteTotal(detalleComandaDTO.getCantidad() * detalleComandaDTO.getPrecioUnitario());

            comandaDTO.addDetallesComanda(detalleComandaDTO);

        }

        comandaDTO.setEstado(Estado.Abierta);
        comandaDTO.setFechaHora(LocalDateTime.now());
        comandaDTO.setNumeroMesa(Integer.parseInt(app.getMesa()));
        comandaDTO.setTotalVenta(200); // 
        comandaDTO.setCliente(app.getClienteSeleccionado());
        System.out.println("Cliente desde nios : " + app.getClienteSeleccionado());

        try {
            app.guardarComanda(comandaDTO);

            JOptionPane.showMessageDialog(this, "Comanda guardada exitosamente", "Éxito", JOptionPane.INFORMATION_MESSAGE);
            if (app.getRol().equals("Administrador")) {
                app.mostrarMenuPrincipal();
            } else {
                app.mostrarMenuMesero();
            }

        } catch (NegocioException ex) {
            JOptionPane.showMessageDialog(this, "Error en la comanda", "Error", JOptionPane.INFORMATION_MESSAGE);
            ex.printStackTrace();
        }
    }

    public void actualizarComandaDetalles() throws NegocioException {
        String folioTemporal = app.getFolioTemporal();
        ComandaDTO comanda = app.buscarComandaPorFolio(folioTemporal);

        DefaultTableModel modeloTabla = (DefaultTableModel) tablaProductosComanda.getModel();
        int filas = modeloTabla.getRowCount();

        List<DetallesComandaDTO> nuevosDetalles = new ArrayList<>();

        for (int i = 0; i < filas; i++) {
            // Obtener los datos de la fila
            String nombre = modeloTabla.getValueAt(i, 0).toString();
            String categoria = modeloTabla.getValueAt(i, 1).toString();
            double precio = Double.parseDouble(modeloTabla.getValueAt(i, 2).toString());
            String comentario = modeloTabla.getValueAt(i, 3).toString();

            // Crear el producto
            ProductoDTO producto = new ProductoDTO();
            producto.setNombre(nombre);
            producto.setTipo(Tipo.valueOf(categoria)); // Asegúrate que coincida con el enum
            producto.setPrecio(precio);

            // Crear el detalle comanda
            DetallesComandaDTO detalleNuevo = new DetallesComandaDTO();
            detalleNuevo.setProducto(producto);
            detalleNuevo.setComentarios(comentario);

            nuevosDetalles.add(detalleNuevo);
        }

        comanda.setDetallesComanda(nuevosDetalles);
        app.actualizarComandaDetalles(comanda);
        JOptionPane.showMessageDialog(this, "Comanda guardada exitosamente", "Éxito", JOptionPane.INFORMATION_MESSAGE);
        if (app.getRol() == "Administrador" && app.isSiguienteComandasActivas() == true) {
            app.mostrarPantallaComandasActivas();
        } else {
            app.mostrarPantallaComandasActivas();
        }

    }

    public void obtenerProductosTemporales() {
        if (app.isSiguienteComandasActivas()) {
            CrearComandaDTO productosComanda = app.getComandaProductosTemporales();
            List<ProductoDTO> productosTemporales = productosComanda.getProductosComanda();
            detallesTemporales = productosComanda.getDetallesComanda();
            DefaultTableModel modeloTabla = (DefaultTableModel) tablaProductosComanda.getModel();
            modeloTabla.setRowCount(0); // Limpia la tabla

            for (int i = 0; i < productosTemporales.size(); i++) {
                ProductoDTO producto = productosTemporales.get(i);
                DetallesComandaDTO detalle = detallesTemporales.get(i);
                modeloTabla.addRow(new Object[]{
                    producto.getNombre(),
                    producto.getTipo().toString(),
                    producto.getPrecio(),
                    detalle.getComentarios()
                });
            }

            calcularTotal();
        } else {

            if (app.getProductoTemporal() == null) {
                // Si no hay producto temporal, simplemente salimos del método
                return;
            }

// Si hay un producto temporal
            ProductoDTO producto = app.getProductoTemporal();

// Verificamos si el tipo de producto es nulo
            String tipoProducto = (producto.getTipo() != null) ? producto.getTipo().toString() : "Tipo no disponible";

// Preparamos el modelo de la tabla
            DefaultTableModel modeloTabla = (DefaultTableModel) tablaProductosComanda.getModel();
            modeloTabla.setRowCount(0); // Limpiamos la tabla

// Añadimos la fila con los datos del producto
            modeloTabla.addRow(new Object[]{
                producto.getNombre(),
                tipoProducto, // Aseguramos que el tipo no sea null
                producto.getPrecio(),
                "Introducir"
            });

// Calculamos el total después de actualizar la tabla
            calcularTotal();

        }

    }

    private void ajustarTamañoFuente(JLabel label, String texto) {
        int tamañoMaximo = 24; // puedes ajustar según el diseño
        int tamañoMinimo = 10; // para evitar que sea ilegible
        Font fuente = label.getFont().deriveFont((float) tamañoMaximo);
        FontMetrics fm = label.getFontMetrics(fuente);

        int anchoDisponible = label.getWidth() - 20; // margen horizontal
        if (anchoDisponible <= 0) {
            return; // aún no está visible el label
        }
        // Reducimos tamaño si es necesario
        while (fm.stringWidth(texto) > anchoDisponible && tamañoMaximo > tamañoMinimo) {
            tamañoMaximo--;
            fuente = fuente.deriveFont((float) tamañoMaximo);
            fm = label.getFontMetrics(fuente);
        }

        label.setFont(fuente);
    }
